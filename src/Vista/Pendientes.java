    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int fila=tabla.getSelectedRow();
        id_cita = Integer.parseInt(tabla.getValueAt(fila,0).toString());
        
        try {
            lblId.setText(con_citas.buscarCita(id_cita).getId_paciente()+"");
        } catch (IOException ex) {
            System.err.println("Error: "+ex);
        }
        
        BloquearBotones(false);
        
        txtNombre.setText(tabla.getValueAt(fila,1).toString());
        txtApellido.setText(tabla.getValueAt(fila,2).toString());
        txtNumero.setText(tabla.getValueAt(fila,3).toString());
        txtMotivo.setText(tabla.getValueAt(fila, 6).toString());
        txtNota.setText(tabla.getValueAt(fila,7).toString());
        txtMonto.setText(tabla.getValueAt(fila,8).toString());
        
        //Separar Fecha
        String[] Fecha = tabla.getValueAt(fila,4).toString().split("-");
        cbDia.setSelectedItem(Fecha[0]);
        cbMes.setSelectedIndex(Integer.parseInt(Fecha[1])-1);
        txtAnho.setText(Fecha[2]);
        //Separar Hora
        String[] Hora = tabla.getValueAt(fila,5).toString().split(":");
        cbHora.setSelectedItem(Hora[0]);
        cbMinutos.setSelectedItem(Hora[1]);  

    }//GEN-LAST:event_tablaMouseClicked

