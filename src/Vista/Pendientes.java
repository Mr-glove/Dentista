    private void pnlCrearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnlCrearMouseClicked
        if(EstaVacio()==true){
            JOptionPane.showMessageDialog(null,"Existe un campo de cita vacio, hagalo denuevo");
        }
        else{
            Cita c = new Cita();
            int id_nuevaCita;


            //Darle un id a la cita
            try {
                List<Cita> c_total = con_citas.leerCitas();
                if(c_total.isEmpty()){
                    c.setId_cita(1);
                } else {
                    id_nuevaCita = c_total.get(c_total.size()-1).getId_cita()+1;
                    c.setId_cita(id_nuevaCita);
                }     
            } catch (IOException ex) {
                System.err.println("Error: "+ex);
            }

            //darle formato a la fecha 
            if(cbMes.getSelectedIndex()<9){
                if(txtAnho.getText().equals(""))
                    c.setFecha(cbDia.getSelectedItem().toString() + "-0" + (cbMes.getSelectedIndex()+1) + "-" + "0000");
                else
                    c.setFecha(cbDia.getSelectedItem().toString() + "-0" + (cbMes.getSelectedIndex()+1) + "-" + txtAnho.getText());

            } else{
                if(txtAnho.getText().equals(""))
                    c.setFecha(cbDia.getSelectedItem().toString() + "-" + (cbMes.getSelectedIndex()+1) + "-" + "0000");
                else
                    c.setFecha(cbDia.getSelectedItem().toString() + "-" + (cbMes.getSelectedIndex()+1) + "-" + txtAnho.getText());
            }


            c.setId_paciente(Integer.parseInt(lblId.getText()));
            c.setHora(cbHora.getSelectedItem().toString() + ":" + cbMinutos.getSelectedItem());
            c.setMotivo(txtMotivo.getText());
            c.setNota(txtNota.getText());
            c.setMonto(txtMonto.getText());
            c.setEstado("Pendiente");


            try {
                con_citas.CrearCitas(c);
                con_citas.LlenarTabla_Pendientes();
            } catch (IOException ex) {
                System.err.println("Error: "+ex);
            }

            Hora();
            Limpiar();
            BloquearBotones(true);
        }
        
    }//GEN-LAST:event_pnlCrearMouseClicked

